'''Crie uma função chamada soma que recebe dois números como parâmetros e retorna a soma deles.
Teste a função com diferentes pares de números.'''

def soma(num1, num2):
    return num1 + num2

result1 = soma(2, 4)
print("Soma de 2 e 4:", result1)

result2 = soma(6, 4)
print("Soma de 2 e 4:", result2)

result3 = soma(7, 3)
